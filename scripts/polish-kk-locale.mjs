function normalize(text) {
    return text.replace(/\u00a0/g, ' ').replace(/\s+/g, ' ').trim();
}

function applyPhrases(text) {
    if (typeof text !== 'string') return text;
    let out = normalize(text);
    for (const [from, to] of PHRASES) {
        if (out.includes(from)) out = out.split(from).join(to);
    }
    for (const [from, to] of Object.entries(EXACT_FIXES)) {
        if (out === from) return to;
    }
    return out;
}

function getOverride(path, key) {
    const dot = path ? `${path}.${key}` : key;
    if (KK_UI_OVERRIDES[dot] !== undefined) return KK_UI_OVERRIDES[dot];
    if (KK_UI_OVERRIDES[key] !== undefined) return KK_UI_OVERRIDES[key];
    return undefined;
}

function walk(obj, path = '') {
    if (typeof obj === 'string') {
        if (KK_UI_OVERRIDES[path] !== undefined) return KK_UI_OVERRIDES[path];
        return applyPhrases(obj);
    }
    if (Array.isArray(obj)) return obj.map((v, i) => walk(v, `${path}[${i}]`));
    if (obj && typeof obj === 'object') {
        const next = {};
        for (const [k, v] of Object.entries(obj)) {
            const childPath = path ? `${path}.${k}` : k;
            next[k] = walk(v, childPath);
        }
        return next;
    }
    return obj;
}

function polishNamespace(namespace) {
    const kkPath = path.join(src, namespace, 'kk.json');
    const raw = JSON.parse(fs.readFileSync(kkPath, 'utf8'));
    let kk = walk(raw);

    if (namespace === 'tonkeeper-web') {
        for (const [k, v] of Object.entries(WEB_OVERRIDES)) {
            if (Object.prototype.hasOwnProperty.call(kk, k)) kk[k] = v;
        }
    }

    if (namespace === 'tonkeeper' && kk.language?.languagesList) {
        kk.language.languagesList.kk = 'Қазақша';
        if (kk.language.list_item) {
            kk.language.list_item.title = 'Тіл';
            kk.language.list_item.value = 'Қазақша';
        }
    }

    fs.writeFileSync(kkPath, JSON.stringify(kk, null, 2) + '\n', 'utf8');
    console.log('Polished', kkPath);
}

